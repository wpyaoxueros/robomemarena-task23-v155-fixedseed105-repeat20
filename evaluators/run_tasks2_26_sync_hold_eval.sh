  echo "[INFO] RUN_ID=${RUN_ID}"
  echo "[INFO] OUT_ROOT=${OUT_ROOT}"
  echo "[INFO] TASKS_JSON=${TASKS_JSON}"
  echo "[INFO] NUM_TRIALS=${NUM_TRIALS} SEED=${SEED} MAX_STEPS=${MAX_STEPS}"
  echo "[INFO] VLM_CKPT=${VLM_CKPT}"
  echo "[INFO] VLA_CONFIG=${VLA_CONFIG}"
  echo "[INFO] VLA_POLICY=${VLA_POLICY}"
  echo "[INFO] VLA_REPO_ID=${VLA_REPO_ID}"
  echo "[INFO] VLA_SERVER_PY=${VLA_SERVER_PY}"
  echo "[INFO] VLA_ACTION_TARGET_MODE=${VLA_ACTION_TARGET_MODE}"
  echo "[INFO] VLA_ABS_EEF_GAIN=${VLA_ABS_EEF_GAIN}"
  echo "[INFO] VLA_ABS_EEF_CLIP=${VLA_ABS_EEF_CLIP}"
  echo "[INFO] ENDPOSE_HOLD_TARGETS_JSON=${ENDPOSE_HOLD_TARGETS_JSON}"
  echo "[INFO] ENDPOSE_TARGET_PASSAGE_COUNTS_JSON=${ENDPOSE_TARGET_PASSAGE_COUNTS_JSON}"
  echo "[INFO] ENDPOSE_HOLD_EEF_DEFAULT_TOL=${ENDPOSE_HOLD_EEF_DEFAULT_TOL}"
  echo "[INFO] ENDPOSE_HOLD_EEF_P95_EXTRA_TOL=${ENDPOSE_HOLD_EEF_P95_EXTRA_TOL}"
  echo "[INFO] ENDPOSE_HOLD_EEF_TOL_CAP=${ENDPOSE_HOLD_EEF_TOL_CAP}"
