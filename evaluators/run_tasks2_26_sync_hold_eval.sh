cleanup() {
  kill "${SERVER_PID}" >/dev/null 2>&1 || true
}
trap cleanup EXIT

for i in $(seq 1 180); do
  if ! kill -0 "${SERVER_PID}" 2>/dev/null; then
    echo "[ERROR] VLA server exited early. See ${SERVER_LOG}" | tee -a "${DRIVER_LOG}"
    tail -n 100 "${SERVER_LOG}" || true
    exit 1
  fi
  if python3 - <<PY >/dev/null 2>&1
import socket
s = socket.socket()
try:
    s.settimeout(1.0)
    s.connect(("127.0.0.1", ${PORT}))
    raise SystemExit(0)
except Exception:
    raise SystemExit(1)
finally:
    s.close()
PY
  then
    echo "[INFO] VLA server ready at try ${i}." | tee -a "${DRIVER_LOG}"
    break
  fi
  sleep 2
done

echo "[INFO] Starting Task2-26 sync endpose-hold eval." | tee -a "${DRIVER_LOG}"
